function M.get_desc(kind, id)
  kind = tostring(kind or '')
  id = tostring(id or '')
  local bucket = M.by_kind_and_id[kind]
  if not bucket then
    return ''
  end
  local direct = bucket[id]
  if direct and direct ~= '' then
    return tostring(direct)
  end
  if kind == 'sample_bond' then
    local mapped = SAMPLE_BOND_ID_ALIAS[id]
    if mapped and bucket[mapped] and bucket[mapped] ~= '' then
      return tostring(bucket[mapped])
    end
  end
  return ''
end

return M
