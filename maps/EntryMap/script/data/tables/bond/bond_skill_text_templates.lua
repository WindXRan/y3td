local function trim(text)
  return tostring(text or ''):gsub('^%s+', ''):gsub('%s+$', '')
end

local function normalize_bond_name(name)
  local key = trim(name):gsub('　', '')
  return BOND_NAME_ALIASES[key] or key
end

local function normalize_math_symbol(text)
  text = tostring(text or '')
  text = text:gsub('([%d%w])%s*[xX]%s*([%d%%])', '%1×%2')
  text = text:gsub('％', '%%')
  return text
end

local function normalize_plain_desc(text)
  text = trim(text)
  if text == '' or text == '无' then
    return ''
  end
  text = normalize_math_symbol(text)
  text = text:gsub('^%[[^%]]*%]%s*', '')
  text = text:gsub('^【[^】]*】%s*', '')
  text = text:gsub('%s+', ' ')
  text = text:gsub('，%s*', '，')
  text = text:gsub('。%s*', '。')
  text = text:gsub('；%s*', '；')
  return text
end

function M.get_activation_desc(bond_name, fallback_desc)
  local canonical = normalize_bond_name(bond_name)
  local templated = ACTIVATION_DESC_BY_BOND[canonical]
  if templated and templated ~= '' then
    return templated
  end
  return normalize_plain_desc(fallback_desc)
end

function M.get_card_desc(card_name, bond_name, fallback_desc)
  local normalized_name = trim(card_name)
  local templated = EXTRA_DESC_BY_CARD[normalized_name]
  if templated and templated ~= '' then
    return templated
  end
  local canonical = normalize_bond_name(bond_name)
  local canonical_by_bond = ACTIVATION_DESC_BY_BOND[canonical]
  if canonical_by_bond and canonical_by_bond ~= '' then
    return normalize_plain_desc(fallback_desc)
  end
  return normalize_plain_desc(fallback_desc)
end

M.activation_desc_by_bond = ACTIVATION_DESC_BY_BOND
M.extra_desc_by_card = EXTRA_DESC_BY_CARD
M.normalize_bond_name = normalize_bond_name

return M
