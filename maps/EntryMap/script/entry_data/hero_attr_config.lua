local function merge_attr_packs(...)
  local merged = {}
  for index = 1, select('#', ...), 1 do
    local pack = select(index, ...)
    if pack then
      for attr_name, value in pairs(pack) do
        merged[attr_name] = value
      end
    end
  end
  return merged
end

